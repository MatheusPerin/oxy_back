CREATE TABLE IF NOT EXISTS EMPRESTIMOLIVRO (
	EMPRESTIMO_ID NUMERIC(20) NOT NULL,
	LIVRO_ID NUMERIC(20) NOT NULL,
	CONSTRAINT PK_EMPRESTIMOLIVRO PRIMARY KEY (EMPRESTIMO_ID, LIVRO_ID),
	CONSTRAINT FK_EMPRESTIMOLIVRO_1 FOREIGN KEY (EMPRESTIMO_ID) REFERENCES EMPRESTIMO(ID),
	CONSTRAINT FK_EMPRESTIMOLIVRO_2 FOREIGN KEY (LIVRO_ID) REFERENCES LIVRO(ID)
);